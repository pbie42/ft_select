#include "ft_select.h"

int						ft_list_len(t_params *list)
{
	t_params			*tmp;
	int					i;

	i = 0;
	tmp = list;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
